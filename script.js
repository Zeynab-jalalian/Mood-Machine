let selects = document.querySelector("select");
let button = document.querySelector("button");
let result = document.querySelector("ul");

button.addEventListener("click", () => {
  let selectedMood = selects.value; // گرفتن حالت انتخاب شده
  if (!selectedMood) return; // اگر چیزی انتخاب نشده بود کاری نکن

  let randomIndex = Math.floor(Math.random() * mood[selectedMood].length); // انتخاب رندوم
  let advice = mood[selectedMood][randomIndex]; // جمله مربوطه

  result.style.display = "block"; // نمایش لیست
  result.innerHTML = `<li>${advice}</li>`; // اضافه کردن جمله به لیست
});
